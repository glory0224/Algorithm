    crypto = list(map(int, input().split()))

    command_len = int(input())

    commands  = input().split()

    cmd = []    # 명령어 정보를 튜플 형태로 담을 리스트

    # i 기준으로 while문 반복하면서 cmd에 명령어 삽입 

    i = 0

    while i < len(commands):
        # I 명령인 경우
        if commands[i] == 'I':
            x = int(commands[i+1])
            y = int(commands[i+2])
            s = list(map(int, commands[i+3:i+3+y]))
            cmd.append(('I', x, y, s))
            i += 3 + y # y의 길이만큼 인덱스 추가
        
        elif commands[i] == 'D':
            x = int(commands[i+1])
            y = int(commands[i+2])
            cmd.append(('D', x, y))
            i += 3

    for c in cmd:

        if c[0] == 'I':
            x, y, s = c[1:]
            for j in range(y):
                crypto.insert(x+j, s[j])
        
        elif c[0] == 'D':
            x, y = c[1], c[2]
            for j in range(y):
                if x+y < len(crypto):
                    crypto.pop(x)
    
    print(f"#{tc} {' '.join(map(str, crypto[:10]))}")

        


